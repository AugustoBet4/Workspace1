package mypackage1;

public class ClaseListadoLocaciones 
{
  String id;
  String locacion;
  String pais;

  public ClaseListadoLocaciones()
  {
  }

  public String getId()
  {
    return id;
  }

  public void setId(String newId)
  {
    id = newId;
  }

  public String getLocacion()
  {
    return locacion;
  }

  public void setLocacion(String newLocacion)
  {
    locacion = newLocacion;
  }

  public String getPais()
  {
    return pais;
  }

  public void setPais(String newPais)
  {
    pais = newPais;
  }
}