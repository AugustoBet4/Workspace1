package mypackage1;

public class ClaseListadoArbitros 
{
  String nombre;
  String telefono;
  String email;
  String idnacionalidad;
  String idarbitro;

  public ClaseListadoArbitros()
  {
  }



  public String getNombre()
  {
    return nombre;
  }

  public void setNombre(String newNombre)
  {
    nombre = newNombre;
  }

  public String getTelefono()
  {
    return telefono;
  }

  public void setTelefono(String newTelefono)
  {
    telefono = newTelefono;
  }

  public String getEmail()
  {
    return email;
  }

  public void setEmail(String newEmail)
  {
    email = newEmail;
  }

  public String getIdnacionalidad()
  {
    return idnacionalidad;
  }

  public void setIdnacionalidad(String newIdnacionalidad)
  {
    idnacionalidad = newIdnacionalidad;
  }

  public String getIdarbitro()
  {
    return idarbitro;
  }

  public void setIdarbitro(String newIdarbitro)
  {
    idarbitro = newIdarbitro;
  }
}