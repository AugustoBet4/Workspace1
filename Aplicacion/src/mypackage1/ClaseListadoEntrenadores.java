package mypackage1;

public class ClaseListadoEntrenadores 
{
  String identrenadores;
  String nombre;
  String edad;
  String idnacionalidad;

  public ClaseListadoEntrenadores()
  {
  }

  public String getIdentrenadores()
  {
    return identrenadores;
  }

  public void setIdentrenadores(String newIdentrenadores)
  {
    identrenadores = newIdentrenadores;
  }

  public String getNombre()
  {
    return nombre;
  }

  public void setNombre(String newNombre)
  {
    nombre = newNombre;
  }

  public String getEdad()
  {
    return edad;
  }

  public void setEdad(String newEdad)
  {
    edad = newEdad;
  }

  public String getIdnacionalidad()
  {
    return idnacionalidad;
  }

  public void setIdnacionalidad(String newIdnacionalidad)
  {
    idnacionalidad = newIdnacionalidad;
  }
}