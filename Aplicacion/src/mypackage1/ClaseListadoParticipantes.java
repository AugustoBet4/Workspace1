package mypackage1;

public class ClaseListadoParticipantes 
{
  String id;
  String nombre;
  String edad;
  String genero;
  String nacionalidad;

  public ClaseListadoParticipantes()
  {
  }

  public String getId()
  {
    return id;
  }

  public void setId(String newId)
  {
    id = newId;
  }

  public String getNombre()
  {
    return nombre;
  }

  public void setNombre(String newNombre)
  {
    nombre = newNombre;
  }

  public String getEdad()
  {
    return edad;
  }

  public void setEdad(String newEdad)
  {
    edad = newEdad;
  }

  public String getGenero()
  {
    return genero;
  }

  public void setGenero(String newGenero)
  {
    genero = newGenero;
  }

  public String getNacionalidad()
  {
    return nacionalidad;
  }

  public void setNacionalidad(String newNacionalidad)
  {
    nacionalidad = newNacionalidad;
  }
}