package mypackage1;

public class ClaseArbitro 
{

  String cod;
  String nombre;
  String telefono;
  String email;
  String nacionalidad;
  String id_nacionalidad;

  public ClaseArbitro()
  {
  }

  public String getCod()
  {
    return cod;
  }

  public void setCod(String newCod)
  {
    cod = newCod;
  }

  public String getNombre()
  {
    return nombre;
  }

  public void setNombre(String newNombre)
  {
    nombre = newNombre;
  }

  public String getTelefono()
  {
    return telefono;
  }

  public void setTelefono(String newTelefono)
  {
    telefono = newTelefono;
  }

  public String getEmail()
  {
    return email;
  }

  public void setEmail(String newEmail)
  {
    email = newEmail;
  }

  public String getNacionalidad()
  {
    return nacionalidad;
  }

  public void setNacionalidad(String newNacionalidad)
  {
    nacionalidad = newNacionalidad;
  }

  public String getId_nacionalidad()
  {
    return id_nacionalidad;
  }

  public void setId_nacionalidad(String newId_nacionalidad)
  {
    id_nacionalidad = newId_nacionalidad;
  }





}